public class CharImageBean  extends BaseBean{
    	private String w;
    	private String path;
    	private String h;
    	private String ext;
    	private String size;
    	private String name;

    public String getW() {
        return w;
    }

    public void setW(String w) {
        this.w = w;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getH() {
        return h;
    }

    public void setH(String h) {
        this.h = h;
    }

    public String getExt() {
        return ext;
    }

    public void setExt(String ext) {
        this.ext = ext;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
